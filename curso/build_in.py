numeros = [45,24,95,26,5,43]

#encontrando el valor mas alto 
numero_mas_alto = max(numeros)
print(numero_mas_alto)

#encontrando el valor mas bajo
numero_mas_bajo = min(numeros)
print(numero_mas_bajo)

 #cualquier dato distinto a 0 nos devuelve true si es 0 nos devuelve false
numero_boolean = bool(0)
print(numero_boolean)

#retorna true si todos los valores son verdaderos
resultado_all = all([243,45,5,[4521,1255,0]])
print(resultado_all)

#suma todos los valores de una iterable
suma_total = sum(numeros)
print(suma_total)

##solo las funciones de sumas o de numeros necesitan numero solamente sin otro tipo de datos
